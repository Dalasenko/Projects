

/**
 * SoundManager handles playing sound effects.
 * In this example, it simply prints messages to the console.
 */
public class SoundManager {

    public static void playShoot() {
        // Simulate shooting sound.
        System.out.println("Pew!");
    }

    public static void playExplosion() {
        // Simulate explosion sound.
        System.out.println("Boom!");
    }
}

